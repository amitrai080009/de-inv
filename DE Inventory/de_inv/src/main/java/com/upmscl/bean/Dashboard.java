package com.upmscl.bean;

public class Dashboard {
	
	private int tenderCount;
	private int circularCount;
	private int userCount;
	private int BlacklistCount;
	
	public int getTenderCount() {
		return tenderCount;
	}
	public void setTenderCount(int tenderCount) {
		this.tenderCount = tenderCount;
	}
	public int getCircularCount() {
		return circularCount;
	}
	public void setCircularCount(int circularCount) {
		this.circularCount = circularCount;
	}
	public int getUserCount() {
		return userCount;
	}
	public void setUserCount(int userCount) {
		this.userCount = userCount;
	}
	public int getBlacklistCount() {
		return BlacklistCount;
	}
	public void setBlacklistCount(int blacklistCount) {
		BlacklistCount = blacklistCount;
	}

}
